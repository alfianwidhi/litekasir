    private void loadKategoriToComboBox() {
        jComboBoxKategori.removeAllItems();
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/litekasir", "root", "")) {
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT id, nama FROM kategori");
            while (rs.next()) {
                jComboBoxKategori.addItem(new KategoriItem(rs.getInt("id"), rs.getString("nama")));
            }
            rs.close();
            st.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal load kategori: " + e.getMessage());
        }
    }

    private void loadMenuToTable() {
        modelMenu.setRowCount(0);
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/litekasir", "root", "")) {
            String sql = "SELECT m.id, m.nama, m.harga, m.stok, k.nama AS kategori FROM menu m JOIN kategori k ON m.id_kategori = k.id";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                modelMenu.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("nama"),
                    rs.getInt("harga"),
                    rs.getInt("stok"),
                    rs.getString("kategori")
                });
            }
            rs.close();
            st.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal load menu: " + e.getMessage());
        }
    }

