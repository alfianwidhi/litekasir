    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih menu yang ingin diupdate!");
            return;
        }
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/litekasir", "root", "")) {
            int idMenu = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
            String nama = jTextFieldNama.getText().trim();
            int harga = Integer.parseInt(jTextFieldHarga.getText().trim());
            int stok = Integer.parseInt(jTextFieldStok.getText().trim());
            KategoriItem kategori = (KategoriItem) jComboBoxKategori.getSelectedItem();
            if (kategori == null) {
                JOptionPane.showMessageDialog(this, "Kategori harus dipilih!");
                return;
            }
            int idKategori = kategori.getId();

            String sql = "UPDATE menu SET nama=?, harga=?, stok=?, id_kategori=? WHERE id=?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, nama);
            ps.setInt(2, harga);
            ps.setInt(3, stok);
            ps.setInt(4, idKategori);
            ps.setInt(5, idMenu);
            ps.executeUpdate();
            ps.close();
            JOptionPane.showMessageDialog(this, "Menu berhasil diupdate!");
            loadMenuToTable();
            clearInput();
        } catch (SQLException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Gagal update menu: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (selectedMenuId == -1) {
            JOptionPane.showMessageDialog(this, "Pilih menu yang ingin dihapus!");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Yakin hapus menu ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/litekasir", "root", "")) {
                String sql = "DELETE FROM menu WHERE id=?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setInt(1, selectedMenuId);
                ps.executeUpdate();
                ps.close();
                JOptionPane.showMessageDialog(this, "Menu berhasil dihapus!");
                loadMenuToTable();
                clearInput();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal hapus menu: " + e.getMessage());
            }
        }   
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String nama = jTextFieldNama.getText().trim();
        String hargaStr = jTextFieldHarga.getText().trim();
        String stokStr = jTextFieldStok.getText().trim();
        KategoriItem kategori = (KategoriItem) jComboBoxKategori.getSelectedItem();

        if (nama.isEmpty() || hargaStr.isEmpty() || stokStr.isEmpty() || kategori == null) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
            return;
        }
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/litekasir", "root", "")) {
            String sql = "INSERT INTO menu (nama, harga, stok, id_kategori) VALUES (?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, nama);
            ps.setInt(2, Integer.parseInt(hargaStr));
            ps.setInt(3, Integer.parseInt(stokStr));
            ps.setInt(4, kategori.getId());
            ps.executeUpdate();
            ps.close();
            JOptionPane.showMessageDialog(this, "Menu berhasil ditambahkan!");
            loadMenuToTable();
            clearInput();
        } catch (SQLException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Gagal tambah menu: " + e.getMessage());
        }   
    }//GEN-LAST:event_btnTambahActionPerformed

